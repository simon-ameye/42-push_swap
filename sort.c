#include "push_swap.h"

void	ft_sort(t_stack *a, t_stack *b)
{
	int i;
	int bitshift;

	bitshift = 0;
	i = 0;
	while (i < a->l - 1)
	{
		if ((((a->v[0]) >> bitshift) & 1) == 0)
			pb(a, b);
		else
			ra(a, b);
		i++;
	}
}